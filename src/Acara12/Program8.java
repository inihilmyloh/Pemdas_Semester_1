package Acara12;

public class Program8 {

    public static void main(String[] args) {
        int i = 10; // Inisialisasi angka mulai dari 10

        // Loop dengan kondisi selama i >= 6
        while (i >= 6) {
            System.out.println("Hello Word " + i); // Cetak "Hello Word" dan angka
            i--; // Kurangi nilai i setiap iterasi
        }
    }
}
