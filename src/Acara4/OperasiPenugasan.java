package Acara4;

public class OperasiPenugasan {
    public static void main(String[] args) {
        
    int a = 5;
    int b;

    System.out.println("a = 4");

    b = a;
    System.out.println("b = a : " + b);

    b += a;
    System.out.println("b += a : " + b);

    b *= a;
    System.out.println("b *= a : " + b);
    }
}
