package Acara4;

public class OperasiRelasional {
    public static void main(String[] args) {
    int a = 5, b = 12;
    
    System.out.println("a adalah " + a + " dan b adalah " + b);
    
    System.out.println("a == b :");
    System.out.println(a == b);
    
    System.out.println("a != b :");
    System.out.println(a != b);
    
    System.out.println("a > b :");
    System.out.println(a > b);

    System.out.println("a < b :");
    System.out.println(a < b);

    System.out.println("a >= b :");
    System.out.println(a >= b);

    System.out.println("a <= b :");
    System.out.println(a <= b);
    }
}
